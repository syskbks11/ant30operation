      SUBROUTINE R_2200
#include      "__DFTF"
C
C                                       * COMMON AREA OF INSTRUCTION   *
#include      "XTMERT"
#include      "XPRCNT"
#include      "XCNUTA"
#include      "XUNTCV"
C
C                                       * WORK VARIABLE DEFINITION     *
C
      INTEGER*2     JC
      REAL*8        DFABRW(5), DATRIG, DC, DWKV(2)
C                                                                       
C----------------------------------------------------------------------C
C PROCEDURE                                                            C
C----------------------------------------------------------------------C
C                                      *-------------------------------*
C                                      * INITIALIZE                    *
C                                      *-------------------------------*
      DWKV(1)   = 0.0D0                                                   
      DWKV(2)   = 0.0D0                                                   
C                                                                       
C                                      *-------------------------------*
C                                      * MEAN OBLIQUITY AT EPOCH       *
C                                      *-------------------------------*
C      DMNOBL =  DCFMNO( 1 )                                             
C     +        + DCFMNO( 2 ) * DCENTR( 1 )                               
C     +        + DCFMNO( 3 ) * DCENTR( 1 )**2                            
C     +        + DCFMNO( 4 ) * DCENTR( 1 )**3                            
C
      DMNOBL = DCFMNO( 1 ) 
     +          + ( DCFMNO( 2 ) 
     +          +   ( DCFMNO( 3 )
     +          +     DCFMNO( 4 )
     +              * DCENTR( 1 )
     +              ) * DCENTR( 1 )
     +            ) * DCENTR( 1 )
C                                                                       
C                                      *-------------------------------*
C                                      * BROWN'S FUNDAMENTAL ARGUMENTS *
C                                      *-------------------------------*
      DO 1000 JC = 1 , 5 , 1                                            
C        DFABRW( JC ) =   DCFBRW( 1 , JC )
C     +                 + DCFBRW( 2 , JC ) * DCENTR(1)
C     +                 + DCFBRW( 3 , JC ) * DCENTR(1)**2
C     +                 + DCFBRW( 4 , JC ) * DCENTR(1)**3
        DFABRW( JC ) = DCFBRW( 3 , JC ) + DCFBRW( 4 , JC ) * DCENTR(1)
        DFABRW( JC ) = DCFBRW( 2 , JC ) + DFABRW( JC ) * DCENTR(1)
        DFABRW( JC ) = DCFBRW( 1 , JC ) + DFABRW( JC ) * DCENTR(1)
 1000 CONTINUE                                                          
C                                                                       
C                                      *-------------------------------*
C                                      * NUTATION IN LONGITUDE AND IN  *
C                                      * OBLIQUITY WITH 106 PERIODICITY*
C                                      *-------------------------------*
      DO 1100 JC = 1 , 106 , 1                                          
        DATRIG =  DCFNUT( 1 , JC ) * DFABRW(1)                          
     +          + DCFNUT( 2 , JC ) * DFABRW(2)                          
     +          + DCFNUT( 3 , JC ) * DFABRW(3)                          
     +          + DCFNUT( 4 , JC ) * DFABRW(4)                          
     +          + DCFNUT( 5 , JC ) * DFABRW(5)                          
        DC     = DCFNUT(6,JC) + DCFNUT(7,JC) * DCENTR(1)
        DWKV(1)= DWKV(1) + DC * DSIN( DATRIG )                          
        DC     = DCFNUT(8,JC) + DCFNUT(9,JC) * DCENTR(1)
        DWKV(2)= DWKV(2) + DC * DCOS( DATRIG )                          
 1100 CONTINUE                                                          
C                                                                       
C                                      *-------------------------------*
C                                      * CONVERTING UNIT TO RADIAN     *
C                                      *-------------------------------*
      DNTLON = DWKV(1) * DACRAD * 1.0D-4
      DNTOBL = DWKV(2) * DACRAD * 1.0D-4
C                                                                       
C                                                                       
      RETURN                                                            
      END                                                               
